int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb < 1)
		return (0);
	if (nb > 2147483647)
		return (0);
	while (i * i < nb && nb <= 46340)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
